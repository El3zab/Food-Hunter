//get parent that contain whole ele, btn of coffe & restaurant
var artical = document.querySelector(".menu-body");
var btns = document.querySelectorAll(".btns button");
var cofeeBtn = document.querySelectorAll(".btns button")[0];

//intial visiblity of menu-nav elements
artical.innerHTML = restoNav;
// func to loop over menu-nav elements if clicked then visible something
let loopMain = (arr, strArr, div, menuNav) => {
    menuNav.forEach((item) => {
        item.addEventListener("click", (el) => {
            div.innerHTML = "";
            arr.forEach((e, i) => {
                strArr.forEach((es, ind) => {
                    el.target.innerHTML == es && i == ind ? div.innerHTML = e : "";
                });
            });
            let oldActive = document.getElementsByClassName("active")[1];
            oldActive.classList.remove("active");
            el.target.classList.add("active");
        });
    });
}

// func to loop over two btn to control what menu-nav that visible & menu-body
btns.forEach((item) => {
    item.addEventListener("click", (eo) => {
        artical.innerHTML = "";
        eo.target == cofeeBtn || eo.target.parentElement == cofeeBtn ? artical.innerHTML = cafeNav : artical.innerHTML = restoNav;
        var div = document.querySelector(".menu-contain");
        var menuNav = document.querySelectorAll(".menu-body ul li");
        if (eo.target == cofeeBtn || eo.target.parentElement == cofeeBtn) {
            // make two array to help the conditon of what will be visile 
            let arr = [cafe, coffe, dessert];
            let strArr = ["cafe", "coffe", "dessert"];
            div.innerHTML = cafe;
            loopMain(arr, strArr, div, menuNav);
        } else {
            // make two array to help the conditon of what will be visile 
            let arr = [all, pasta, burger, pizza, salad, special];
            let strArr = ["all", "pasta", "burger", "pizza", "salad", "special"];
            div.innerHTML = all;
            loopMain(arr, strArr, div, menuNav);
        }

    });
});


